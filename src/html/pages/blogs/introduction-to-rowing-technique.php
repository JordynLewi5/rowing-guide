                        <h2>The Sequence</h2>
                        <p>The rowing stroke consists of different phases. It starts at the catch position where the rower's knees are bent, shins vertical, body leaned forward, and arms extended forward with oar blades in the water.</p>
                        <p>From there, the rower initiates the drive phase. They begin by explosively pushing with their legs, extending them fully. As the legs finish extending, the rower swings their torso backward from the hips while maintaining a straight back. This transfers the power from the legs to the upper body. Finally, the rower pulls the oar handle towards their body using their arms, propelling the boat forward.</p>
                        <p>Once the rower has completed the drive phase, they reach the finish position. Here, the legs are fully extended, the body leans back slightly, and the oar blades are lifted out of the water. </p>
                        <p>To prepare for the next stroke, the rower enters the recovery phase. It involves extending the arms forward, swinging the body forward from the hips while keeping the back straight, sliding the seat up the slide by bending the knees, and finally reaching the catch position again to start the cycle anew.</p>
                        
                        <hr>

                        <h2>Thinking Differently</h2>
                        Getting your rowers to think differently about the rowing motion will create important changes in a short period without physically making any changes.

                        <h3>Push Don’t Pull</h3>
                        <p>Push the machine or boat away rather than pulling the handle. When we think about pushing, it is more intuitive to engage the legs to generate power. Additionally, pulling on the handle may lead to early back and arm movement or “death gripping”. For some rowers, this may be a turning point in their understanding of the rowing motion, so emphasize this!</p>

                        <h3>Staying Firm and Stable, Yet Relaxed and Fluid</h3>
                        <p>A common mistake that many novice rowers will make is that when they are performing a stroke they want to tense everything up and pull as hard as possible, without much thought to the effects it will have on efficiency and the set of the boat, among other technical issues like blade digging or washing out/missing water. You want your rowers to have a loose yet locked grip on the oar handle right at the base of the fingers and palm. Do not squeeze the handle. When they begin their drive phase there should be no change in the positioning of the handle within their grip, otherwise they will tire out their forearms, hands, etc. Every movement should be fluid, relaxed, and focused. During the leg drive they should maintain a constant body position, one that is not overextending or underextending (if you imagine their body as a hand on a clock, it should point to 11 o’clock), and should act as a solid foundation to transfer the force from the legs to the oars.</p>

                        <hr>

                        <h2>Foundational Points</h2>

                        <p>It’s important to set your rowers up for success right off the gun. The following points are fundamental aspects of the rowing motion that will get your rowers on the right track to being more efficient on the erg and in the future on the water.</p>

                        <h3>Getting Them Set Up Correctly on the Machine</h3>

                        <p>Feet: Strap across the widest part of their feet.</p>
                        <p>Seat: Sit generally to the center of the seat and have them rotate their hips forward to be a solid stepping stone for the back during the stroke.</p>
                        <p>Handle and hands: Hands wide with the pinkies still holding on to the edge of the handle, thumbs on the underside, and keep a relaxed grip.</p>

                        <h3>Train the Catch Position</h3>

                        <p>The catch is arguably the single most important part of the stroke. The following is a checklist of things to look for in the catch position.</p>
                        <ul>
                            <li>
                                Hand position
                                <p>- Hands wide apart with pinkies on the edge of the handle.</p>
                            </li>
                            <li>
                                Arms locked out
                                <p>- Crease slightly upwards.</p>
                            </li>
                            <li>
                                Shoulders reaching 
                                <p>- Relaxed and allowing the tension to hold them forward.</p>
                                <p>- Use the lats the support your arms during the drive (Do NOT engage your shoulders)</p>
                            </li>
                            <li>
                                Head & neck relaxed and level
                                <p>- Try to avoid recruiting muscles that don’t need to be engaged.</p>
                            </li>
                            <li>
                                Back neutral & flat
                                <p>- Not rounded, or curving inward.</p>
                                <p>- Back should be IN LINE with the hips.</p>
                            </li>
                            <li>
                                Hips behind shoulders
                                <p>- This will put their body at 11 o’clock.</p>
                            </li>
                            <li>
                                Shins should be as close to perpendicular to the ground or water as possible
                                <p>- Do not move further past this angle.</p>
                                <p>- Work on calf flexibility and mobility to allow the heel to be firmly planted down while at the catch.</p>
                            </li>
                        </ul>

