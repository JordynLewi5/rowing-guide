                    <div class="blogs-body">
                        <img src="/src/media/images/drills.jpg" style="width:100%">
                        <p>Drills are important for emphasizing proper form and timing, make sure you ensure the rowers apply these drills to their actual rowing.</p>
                        <h3>Reverse Pick Drill (Erg & Water Drill)</h3>
                        <p>This drill works on separating out the drive sequence into its 3 main components. Begin from the catch and have your rowers row with legs only on the square. It is extremely important that the rowers are able to do this while maintaining a constant forward body position with their arms fully extended. It also helps to remind them that it will feel very strange and that it, in fact, should feel strange. After around 20 strokes, have them add in the body portion for another 20 strokes. Then add in arms for 20 more strokes and finally, have them add in the feather.</p>
                        <hr>
                        <h3>Pick Drill (Erg & Water Drill)</h3>
                        <p>This drill also works on separating out the drive sequence into its 3 main components, however it is the less preferred of the two pick drill variants. Begin from the finish and have your rowers row with arms only on the square. After around 20 strokes, have them add in the body portion for another 20 strokes. Then add in legs. You may choose to start with half slide and then move onto full slide, but this is less important. And finally, have them add in the feather.</p>
                        <hr>
                        <h3>Legs Only (Erg & Water Drill)</h3>
                        <p>This drill emphasizes the utilization of the legs during the rowing motion while keeping the body and arms solid, but relaxed.</p>
                        <p>You will tell your rower to get up to the catch. Have them drive their legs down into the machine while maintaining the same body and arm structure that they had while they were at the catch. You can start with half slides to practice the initial engagement of the leg muscles, and once they seem comfortable with that, you can move on to full slides. Moving back up to the catch, they will maintain the same exact structure.</p>
                        <p><a href="https://www.youtube.com/watch?v=SRlXnpSNf3A" target="__">"How to Row - The Single Greatest Drill Ever"</a></p>
                        <hr>
                        <h3>Legs and Body (Erg & Water Drill)</h3>
                        <p>The legs and body drill is an extension of the legs-only drill. It will teach your rower how to transition from the leg drive phase into the back swing</p>
                        <p>You will begin by running the legs only drill found above. Once your rower is comfortable with the leg drive, you can add in the body. Emphasize the rotation around the hips, making sure the lumbar lays a strong foundation for the rest of the back and stays in line with the back.</p>
                        <p><a href="https://www.youtube.com/watch?v=SRlXnpSNf3A" target="__">"How to Row - The Single Greatest Drill Ever"</a></p>
                        <hr>
                        <h3>Cut the Cake (Water Drill)</h3>
                        <p>The cut the cake drill is a great way to practice the swing on the recovery and getting the body prepared for a solid catch. It's also good for people whose knees come up early on the recovery or who pause their hands at the finish.</p>
                        <p>You will tell your rower to get up to the catch. They will drive their legs down, followed by the back swing to 11 o’clock, and then pull in the handle with their arms. Once they are at the finish, they will extend their arms, followed by swinging their back forward (around their hips, making sure the lumbar is not rotated back) to about 1 o’clock. Once they finish swinging their body over, they will then swing their body back to the finish position, followed by bringing in the arms, and then they will complete a full recovery sequence (arms, body, legs), and start the motion over until the drill is over.</p>
                        <p><a href="https://www.youtube.com/watch?v=dsBbRqsP9e4" target="__">"Fundamental Rowing Drills: Cut the Cake"</a></p>
                        <hr>
                        <h3>Feet-out Drill (Erg Drill)</h3>
                        <p>This drill is aimed to keep your feet on the foot stretcher without actually having your feet strapped in. It helps you rely more on your core rather than the straps which leads to inefficiencies, especially when on the water. This drill is great for self-teaching as well, so many rowers will naturally make adjustments to try to stay on the stretcher.</p>
                        <p>The instruction for this drill is pretty simple, all you have to do is have your rowers have their feet out of the straps and begin rowing full strokes. You can have them row like this for the duration of warming up.</p>
                        <p>Another way to run this drill is to have them start at the finish of the stroke, rowing arms only. Then once they seem comfortable with that, add in backs, then half slide, then ¾ slide, and finally full slide rowing.</p>
                        <p><a href="https://www.youtube.com/watch?v=PbGqn5BmZkc">"Let's do this: The Feet Out Drill: The Cure All For (nearly) All Your Rowing Technique Woes"</a></p>
                        <hr>
                        <h3>Table Tops (Mental Drill)</h3>
                        <p>This drill isn’t really a drill, in the sense that you aren’t physically changing up the stroke, it’s more of an idea or a way to flip a switch in your rowers heads. Tell your rowers to imagine that their hands, the tips of their fingers or their palms are gliding across the face of a smooth table top. The idea is to emphasize the importance of maintaining an even handle height throughout the stroke.</p>
                        <hr>
                        <h3>Relaxed Feather (Water Drill)</h3>
                        <p>Allowing the water to feather the blade every other stroke to emphasize how relaxed you should be at the finish.</p>
                        <p><a href="https://www.youtube.com/watch?v=cP0bMkApHT0">"Tips from an Olympic Coach on how to imporve your sculing crew"</a></p>
                        <hr>
                        <h3>No Pressure Race Pace (Water Drill)</h3>
                        <p>This drill works on preparation for the catch by emphasizing the qualities of a good recovery. If you are not properly prepared for the catch or if you’re not getting an effective recovery, the boat speed will not match the effort being put into the boat. The idea is to give yourself little time to get up to the catch with little boat speed. Working the sequence effectively is vital in this drill because the goal is to disturb the boat as little as possible.</p>
                        <p><a href="https://www.youtube.com/watch?v=phlHavrK9zU&ab_channel=AramTraining">"The most effective race pace drill: How to kep your boat speed alive"</a></p>
